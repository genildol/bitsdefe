      <h2 class="display-8 mb-4">A Igreja, o que é igreja e ser igreja</h2>

      <p>O povo de Deus é chamado a viver em comunidade, a ser igreja. A igreja não é apenas um edifício, mas sim a reunião dos fiéis que buscam adorar a Deus e servir ao próximo. É um lugar de acolhimento, ensino e crescimento espiritual. A Bíblia nos mostra a importância da comunhão em Hebreus 10:25, que nos exorta a não deixarmos de nos congregar, e em <a href="" target="_blank">Atos 2:42</a> , que descreve a vida da primeira igreja, perseverando na doutrina dos apóstolos, na comunhão, no partir do pão e nas orações.</p>

      <p>Ser igreja é viver em comunhão com outros crentes, compartilhando a fé, os valores e os ensinamentos de Jesus Cristo. É um chamado para amar ao próximo, ajudar os necessitados e ser luz no mundo. A importância do amor e da caridade é enfatizada em João 13:34-35, onde Jesus diz que o amor uns pelos outros é a marca de seus discípulos. Além disso, Mateus 5:14-16 nos chama a ser a luz do mundo e a deixar que a nossa luz brilhe diante dos homens.</p>

      <p>O início da igreja foi formado com a vinda do Espírito Santo no dia de Pentecostes, quando os discípulos de Jesus foram capacitados a pregar o evangelho e a formar a primeira comunidade cristã. Essa poderosa manifestação do Espírito Santo é descrita em Atos 2:1-4.</p>

      <p>A igreja é chamada a ser o corpo de Cristo na Terra, a continuar sua missão de amor e serviço. Cada membro tem um papel único e importante a desempenhar, e juntos formamos uma comunidade que reflete a glória de Deus. A analogia da igreja como o corpo de Cristo é descrita em 1 Coríntios 12:27, que afirma: "Ora, vós sois o corpo de Cristo, e seus membros em particular". Efésios 4:16 também destaca como cada parte do corpo, ou seja, cada crente, contribui para o crescimento e edificação do todo em amor.</p>

      <p>Como citado acima, a igreja não é templo, não é ter, fazer a construção de algo físico na terra, vai muito mais além. É seguir o exemplo de Jesus Cristo e viver em amor e serviço ao próximo. A visão de que o corpo do cristão é o verdadeiro templo de Deus é apresentada em 1 Coríntios 6:19-20: "Ou não sabeis que o vosso corpo é o templo do Espírito Santo, que habita em vós, proveniente de Deus, e que não sois de vós mesmos? Porque fostes comprados por bom preço; glorificai, pois, a Deus no vosso corpo, e no vosso espírito, os quais pertencem a Deus."</p>

      <p>Portanto, ser igreja é um chamado para todos os cristãos, um convite para viver em comunhão com Deus e com os outros, e para ser uma luz no mundo, refletindo o amor e a graça de Deus em todas as áreas da vida. A essência desse chamado é resumida em Mateus 28:19-20, a Grande Comissão, onde Jesus instrui seus seguidores a ir e fazer discípulos de todas as nações, batizando-os e ensinando-os a obedecer a tudo o que Ele ordenou.</p>

      <p>Os discípulos juntamente com os apóstolos foram fundamentais na formação da igreja primitiva, pregando o evangelho e estabelecendo comunidades de fé em todo o mundo. Eles foram os responsáveis por levar a mensagem de Cristo a todos os cantos da Terra. O livro de Atos dos Apóstolos, em especial Atos 1:8, descreve o mandato de Jesus para que seus apóstolos fossem suas testemunhas "em Jerusalém, em toda a Judeia e Samaria, e até aos confins da terra", o que vemos se cumprir na jornada transformadora de Pedro e dos demais apóstolos.</p>

