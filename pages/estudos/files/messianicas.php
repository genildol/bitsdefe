        <h2 class="display-8 mb-4">O que são promessas messiânicas?</h2>
        <p>As promessas messiânicas são previsões e declarações encontradas no Antigo Testamento que apontam para a vinda de um Messias, um Salvador prometido por Deus. Desde o princípio, Deus anunciou esse plano redentor, prometendo aquele que venceria o pecado e o mal: “E porei inimizade entre ti e a mulher, e entre a tua semente e a sua semente; esta te ferirá a cabeça” (Gênesis 3:15). Essas promessas são fundamentais para a compreensão da fé cristã, pois apontam para Jesus Cristo como o cumprimento das profecias: “Porque todas quantas promessas há de Deus, são nele sim, e nele amém” (2 Coríntios 1:20).</p>

        <p>As promessas messiânicas descrevem o Messias como Rei justo, Libertador, Servo sofredor e Redentor. Ele é apresentado como Rei davídico: “E o seu nome será: Senhor Justiça Nossa” (Jeremias 23:5-6), e como Libertador prometido: “O Espírito do Senhor Deus está sobre mim… enviou-me a restaurar os contritos de coração” (Isaías 61:1). O Messias também é descrito como Servo sofredor: “Mas ele foi ferido pelas nossas transgressões” (Isaías 53:5), e como Redentor: “E virá um Redentor a Sião” (Isaías 59:20). Essas profecias aparecem em livros como Isaías, Jeremias, Miquéias e Salmos (Isaías 7:14; Miquéias 5:2; Salmos 22:16).</p>

        <p>As Escrituras também anunciam detalhes específicos da obra messiânica, como o nascimento virginal: “Eis que a virgem conceberá, e dará à luz um filho” (Isaías 7:14); a linhagem davídica: “Do fruto do teu ventre porei sobre o teu trono” (Salmos 132:11); o ministério de cura e ensino: “Então os olhos dos cegos se abrirão” (Isaías 35:5); a morte sacrificial: “A sua alma ele dará como expiação pelo pecado” (Isaías 53:10); e a ressurreição: “Pois não deixarás a minha alma no inferno” (Salmos 16:10).</p>

        <p>Essas promessas são significativas porque revelam a fidelidade de Deus em cumprir Sua palavra ao longo da história: “De todas as boas palavras que o Senhor falara… nenhuma caiu em terra” (Josué 21:45). Elas também revelam o plano de Deus para a redenção da humanidade, oferecendo esperança e salvação a todos os que creem no Messias prometido: “Mas o justo viverá da sua fé” (Habacuque 2:4), cumprido plenamente em Jesus Cristo (Lucas 24:44).</p>


